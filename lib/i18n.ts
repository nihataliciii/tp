export function t(lang: Language, key: keyof typeof translations['en'], params?: Record<string, string | number>) {
  let str = translations[lang][key] || translations['en'][key] || key;
  if (params) {
    Object.keys(params).forEach(p => {
      str = str.replace(`{${p}}`, String(params[p]));
    });
  }
  return str;
}
